public void drawPolygon(int sides, int radius)
{
  //equation for sidelength given the radius of a polygon
double angle = 360/sides;
double n = 180/sides;
double nRad = Math.sin(Math.toRadians(n));
int sideLength =(int) (2 * radius * nRad);

while (sides > 0){
this.forward(sideLength);
this.turn(angle);
sides -= 1;}
}
